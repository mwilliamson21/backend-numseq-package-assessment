def square(n):
    """Square a passed in integer and return it"""
    squared = n*n
    return squared


def triangle(n):
    """Finding numbers which build a triangle when you stack it."""
    triangle = (n * (n + 1)) / 2
    return triangle


def cube(n):
    """Cube a number, return the cubed number, and live happily ever after"""
    cube = n ** 3
    return cube
